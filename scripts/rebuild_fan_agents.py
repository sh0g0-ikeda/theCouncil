def build_argument_arsenal(topics: list[dict[str, Any]]) -> list[dict[str, Any]]:
    arsenal = []
    for idx, item in enumerate(topics[:5]):
        arsenal.append(
            {
                "id": item["arsenal_id"],
                "desc": item["arsenal_desc"],
                "phase_bias": list(PHASE_BIASES[idx]),
                "cooldown": COOLDOWNS[idx],
            }
        )
    return arsenal


def filtered_must_distinguish_from(spec: dict[str, Any]) -> dict[str, str]:
    return {
        agent_id: text
        for agent_id, text in spec["must_distinguish_from"].items()
        if agent_id not in EXCLUDED_FAN_AGENT_IDS
    }


def build_persona(agent_id: str, spec: dict[str, Any]) -> dict[str, Any]:
    return {
        "id": agent_id,
        "display_name": spec["display_name"],
        "label": spec["label"],
        "worldview": spec["worldview"],
        "combat_doctrine": spec["combat_doctrine"],
        "blindspots": spec["blindspots"],
        "speech_constraints": spec["speech_constraints"],
        "must_distinguish_from": filtered_must_distinguish_from(spec),
        "argument_arsenal": build_argument_arsenal(spec["topics"]),
        "debate_function_preference": spec["debate_function_preference"],
        "forbidden_patterns": FORBIDDEN_PATTERNS,
        "ideology_vector": spec["ideology_vector"],
        "categories": spec["categories"],
    }


def build_chunk_texts(spec: dict[str, Any], item: dict[str, Any]) -> list[str]:
    subject = spec["subject_name"]
    worldview = spec["worldview"]
    doctrine = spec["combat_doctrine"]
    blindspots = spec["blindspots"]
    distinctions = list(filtered_must_distinguish_from(spec).values()) or [
        "近い立場でも、何を最優先の制約と見るかで結論は大きく変わる。"
    ]
    first_distinction = distinctions[0]
    second_distinction = distinctions[1] if len(distinctions) > 1 else distinctions[0]
    non_negotiable = spec["speech_constraints"]["non_negotiable"]
    return [
        (
            f"{item['topic']}を{subject}で押さえるなら、芯は{item['premise']}という一点だ。"
            f"{worldview[0]} {doctrine[0]} {item['decision']} "
            f"{worldview[2]} {item['contrast']}"
        ),
        (
            f"{subject}は{item['topic']}を語るとき、{item['mechanism']}"
            f" {worldview[1]} {doctrine[1]} {item['decision']}"
        ),
        (
            f"{item['topic']}でよく出る批判は、{item['counter']}"
            f" {subject}にとって問題は評判ではなく、何が現実を動かす制約かを見誤ることだ。"
            f" {blindspots[0]}という弱点を抱えても、{doctrine[2]}"
        ),
        (
            f"{subject}の判断基準では、{item['decision']}"
            f" だから{blindspots[0]}という弱点を抱えても、{non_negotiable}"
            f" その前提で{item['mechanism']}"
        ),
        (
            f"{item['topic']}に関するこの立場の時系列はこうだ。{item['era']}"
            f" ここでも{worldview[2]} {item['decision']}"
        ),
        (
            f"{item['topic']}で{subject}を取り違えると、論点の芯を外す。{item['contrast']}"
            f" 近い立場との違いは、{first_distinction}"
            f" さらに言えば、{second_distinction}"
        ),
    ]


def ensure_chunk_length(text: str, spec: dict[str, Any], item: dict[str, Any]) -> str:
    extras = [
        spec["worldview"][0],
        spec["worldview"][1],
        spec["worldview"][2],
        spec["combat_doctrine"][0],
        spec["combat_doctrine"][1],
        spec["combat_doctrine"][2],
        spec["blindspots"][1],
        item["decision"],
        item["contrast"],
        item["counter"],
    ]
    result = text
    for extra in extras:
        if len(result) >= 150:
            break
        if extra not in result:
            result = f"{result} {extra}"
    return result


def write_agent(agent_id: str, spec: dict[str, Any]) -> None:
    agent_dir = AGENTS_DIR / agent_id
    agent_dir.mkdir(parents=True, exist_ok=True)

    persona = build_persona(agent_id, spec)
    (agent_dir / "persona.json").write_text(
        json.dumps(persona, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )

    chunks: list[str] = []
    for item in spec["topics"]:
        for text in build_chunk_texts(spec, item):
            normalized_text = ensure_chunk_length(text, spec, item)
            chunks.append(
                json.dumps(
                    {
                        "topic": item["topic"],
                        "tags": item["tags"],
                        "text": normalized_text,
                    },
                    ensure_ascii=False,
                )
            )
    (agent_dir / "chunks.jsonl").write_text("\n".join(chunks) + "\n", encoding="utf-8")


def main() -> None:
    AGENTS_DIR.mkdir(parents=True, exist_ok=True)
    active_specs = {
        agent_id: spec
        for agent_id, spec in FAN_SPECS.items()
        if agent_id not in EXCLUDED_FAN_AGENT_IDS
    }
    for agent_id, spec in active_specs.items():
        write_agent(agent_id, spec)
    print(f"rebuilt {len(active_specs)} fan agents")


if __name__ == "__main__":
    main()
