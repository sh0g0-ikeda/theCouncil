def seed_topic(
    name: str,
    tags: list[str],
    premise: str,
    era: str,
    arsenal_id: str,
    arsenal_desc: str,
    *,
    mechanism: str | None = None,
    counter: str | None = None,
    decision: str | None = None,
    contrast: str | None = None,
) -> dict[str, Any]:
    return {
        "topic": name,
        "tags": tags,
        "premise": premise,
        "mechanism": mechanism or premise,
        "counter": counter or f"{premise}という筋を崩さずに、短期の反発より長期の構造を見ろと返す。",
        "decision": decision or "最優先の制約は見栄えではなく、長期の構造を握れているかだ。",
        "era": era,
        "contrast": contrast or "似た陣営でも、どの制約を最優先に置くかで結論は変わる。",
        "arsenal_id": arsenal_id,
        "arsenal_desc": arsenal_desc,
    }


